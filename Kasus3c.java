public class Kasus3c {
    public static void main(String[] args) {
        //Kasus perbandingan
        int a=5, b=3, c=5;

        System.out.println();
        System.out.println("Nilai a = "+a);
        System.out.println("Nilai b = "+b);
        System.out.println("Nilai c = "+c);
        System.out.println();

        if(a==c)
            System.out.println("a dan c adalah sama");
        if(a!=b)
            System.out.println("a dan b adalah tidak sama");
        if(a>b)
            System.out.println("a lebih besar dari b");
        if(b<c)
            System.out.println("b lebih kecil dari c");
    }
}
